package lessons_1

fun main() {
    val crystalOre = 7
    val ironOre = 11
    val buff = 20
    val calculatingThePercentage = 100


    val buffCrystalOre = (buff * crystalOre) / calculatingThePercentage
    val buffIronOre = (buff * ironOre) / calculatingThePercentage

    println(buffCrystalOre)
    println(buffIronOre)
}